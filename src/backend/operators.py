def add(a,b):
    """
    Réalise l'addition 
    Paramètres : a, b => ici ce n'est pas un type en particulier 
    Retourne: Le resultat de l'addition a+b
    """
    return a + b

def subtract(a,b):
    """
    Réalise la soustraction 
    Paramètres : a, b => ici ce n'est pas un type en particulier
    Retourne: le resultat de la soustraction a-b
    """
    return a - b

def multiply(a,b):
    """
    Réalise la multiplication 
    Paramètres : a, b => ici ce n'est pas un type en particulier.
    Retourne: le resultat de la multiplication a*b
    """
    return a * b

def divide(a,b):
    """
    Réalise la division 
    Paramètres : a, b => ici ce n'est pas un type en particulier.
    Retourne: le resultat de la division a/b
    """
    return a / b
